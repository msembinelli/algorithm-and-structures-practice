/*****************************************************************************
 *                                 Includes                                  *
 *****************************************************************************/
#include <stdlib.h>
#include <stdbool.h>

/*****************************************************************************
 *                                  Types                                    *
 *****************************************************************************/
// Public queue structure
typedef struct
{
    void* private;
} queue;

/*****************************************************************************
 *                             Public Prototypes                             *
 *****************************************************************************/
queue* init(int capacity);
void enqueue(queue* q, int data);
int dequeue(queue* q);
bool empty(queue* q);
bool full(queue* q);
void destroy(queue* q);
