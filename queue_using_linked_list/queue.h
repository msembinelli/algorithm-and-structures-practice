/*****************************************************************************
 *                                 Includes                                  *
 *****************************************************************************/
#include <stdlib.h>
#include <stdbool.h>

/*****************************************************************************
 *                                  Types                                    *
 *****************************************************************************/
// Public queue structure
typedef struct
{
    void* private;
} queue;

/*****************************************************************************
 *                             Public Prototypes                             *
 *****************************************************************************/
queue* init_queue(void);
void enqueue(queue* q, int key);
int dequeue(queue* q);
bool empty_queue(queue* q);
void destroy_queue(queue* q);
